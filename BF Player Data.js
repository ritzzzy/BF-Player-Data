    function updateMarkets() {
        const nameEls = document.querySelectorAll("p[class$='runnerName']");
        nameEls.forEach(el => {
            let originalName = el.textContent.trim().replace(/^\[.*?\]\s*/, '');
            const data = playerData[originalName];
            if (data) {
                const newLabel = `[#${data.number} ${data.team}] ${originalName}`;
                if (el.textContent !== newLabel) {
                    el.textContent = newLabel;
                }
            }
        });
    }

